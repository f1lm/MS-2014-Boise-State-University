function preprocess(msgid){
  if (typeof(window.preprocessor) == 'undefined'){
    return msgid;
  }
  var value = window.preprocessor[msgid];
  return typeof(value) == 'undefined' ? msgid : value;
}

function gettext(msgid, interpolation) {
  msgid = preprocess(msgid);
  var value = catalog[msgid];

  if (typeof(value) == 'undefined') {
    value = msgid;
  } else {
    value = (typeof(value) == 'string') ? value : value[0];
  }

  if (interpolation) {
    value = window.interpolate(value, interpolation, !(length in interpolation));
  }

  return value;
}

function ngettext(singular, plural, count) {
  singular = preprocess(singular);
  plural = preprocess(plural);
  value = catalog[singular];
  if (typeof(value) == 'undefined') {
    return (count == 1) ? singular : plural;
  } else {
    return value[pluralidx(count)];
  }
}

function gettext_noop(msgid) { return msgid; }

function pgettext(context, msgid) {
  msgid = preprocess(msgid);
  var value = gettext(context + '' + msgid);
  if (value.indexOf('') != -1) {
    value = msgid;
  }
  return value;
}

function npgettext(context, singular, plural, count) {
  singular = preprocess(singular);
  plural = preprocess(plural);
  var value = ngettext(context + '' + singular, context + '' + plural, count);
  if (value.indexOf('') != -1) {
    value = ngettext(singular, plural, count);
  }
  return value;
}

function interpolate(fmt, obj, named) {
  if (named) {
    return fmt.replace(/%\(\w+\)s/g, function(match){return String(obj[match.slice(2,-2)])});
  } else {
    return fmt.replace(/%s/g, function(match){return String(obj.shift())});
  }
}

