package de.must.applet;

/**
 * Container of remote elements to receive values.
 * @author Christoph Mueller
 */
public interface ValueDelegator {

  /**
   * Checks all remote elements of this container and sets the value if
   * the ID of the remote element fits.
   * @param id  the ID of the remote element to set its value
   * @param value  the new value of the remote element
   * @return true if the remote element was part of this container
   */
  public boolean setValues(String id, String value);

}
